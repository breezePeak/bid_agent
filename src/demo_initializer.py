    company_path = root / "sources" / "company" / "demo_company.md"

    created: dict[str, str] = {}

    if tender_path.exists():
        print(f"[跳过] {tender_path} 已存在，不覆盖")
    else:
        ensure_file(tender_path, DEMO_TENDER)
        print(f"[创建] {tender_path}")
    created["demo_tender"] = str(tender_path.relative_to(root))

    if company_path.exists():
        print(f"[跳过] {company_path} 已存在，不覆盖")
    else:
        ensure_file(company_path, DEMO_COMPANY)
        print(f"[创建] {company_path}")
    created["demo_company"] = str(company_path.relative_to(root))

    return created
